//How to use Foreach (aslo known as Enchanced for Loop)
public class JavaForeach_5 {
    public static void main(String[] args) {
        //Declaring an array
        int arr[]={12,23,44,56,78};
        //Printing array using for-each loop
        for(int i:arr){
            System.out.println(i);
        }
    }
}
